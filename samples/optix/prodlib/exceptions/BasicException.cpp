#include <prodlib/exceptions/BasicException.h>

#include <string>

#ifdef _WIN32
#include <windows.h>
#endif

using namespace prodlib;


BasicException::BasicException( const ExceptionInfo& exceptionInfo, const std::string& desc )
    : Exception( exceptionInfo )
    , m_description( desc )
{
}

std::string BasicException::getDescription() const
{
    return m_description + getInfoDescription();
}
