// clang-format on

class PtxEncryption : public testing::Test
{
  public:
    void SetUp() override;
    void TearDown() override;

  protected:
    std::unique_ptr<optix::Context> m_context;
    RTcontext                       m_rtcontext;
    std::string                     m_ptx;
    std::string                     m_preEncryptedPtx;
};

void PtxEncryption::SetUp()
{
    m_context   = std::unique_ptr<optix::Context>( new optix::Context() );
    m_rtcontext = reinterpret_cast<RTcontext>( m_context.get() );

    std::ifstream     input_file( ptxPath( "test_Context", "sphere.lw" ) );
    std::stringstream str;
    str << input_file.rdbuf();
    m_ptx = str.str();

    m_preEncryptedPtx = std::string( &preEncryptedPtx[0], &preEncryptedPtx[0] + preEncryptedPtxSize );
}

void PtxEncryption::TearDown()
{
    m_context->tearDown();
}

TEST_F( PtxEncryption, strongVariant )
{
    optix::lwca::ComputeCapability target_max( 9999 );

    // Compile unencrypted PTX
    optix::Program program1( m_context.get() );
    EXPECT_NO_THROW( program1.createFromString( m_ptx.c_str(), "(api input string)", "intersect", target_max ) );

#if 0  // Disabled because the failure seems to affect some global state of the PTX parser (bug 2613371)
    // Compile pre-encrypted PTX (fails, encryption not yet enabled)
    optix::Program program2( m_context.get() );
    EXPECT_THROW( program2.createFromString( m_preEncryptedPtx, "(api input string)", "intersect", target_max ), std::exception );
#endif

    // Enable encryption and encrypt PTX
    EncryptionHelper ptxEncryption( m_rtcontext, vendorPublicKey, vendorSecretKey );
    std::string      encryptedPtx = ptxEncryption.encrypt( m_ptx );

    // Compile encrypted PTX
    optix::Program program3( m_context.get() );
    EXPECT_NO_THROW( program3.createFromString( encryptedPtx.c_str(), "(api input string)", "intersect", target_max ) );

#if 0  // Disabled because the failure seems to affect some global state of the PTX parser (bug 2613371)
    // Compile pre-encrypted PTX (fails, wrong session key)
    optix::Program program4( m_context.get() );
    EXPECT_THROW( program4.createFromString( m_preEncryptedPtx, "(api input string)", "intersect", target_max ), std::exception );
#endif

    // Compile unencrypted PTX (fails, not allowed)
    optix::Program program5( m_context.get() );
    EXPECT_THROW( program5.createFromString( m_ptx.c_str(), "(api input string)", "intersect", target_max ), std::exception );

    // Compile internal, unencrypted PTX
    EXPECT_NO_THROW( m_context->getSharedProgramManager()->getTrivialExceptionProgram() );
}

