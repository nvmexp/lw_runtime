/*
 * This file is used for setting per-link, per-lane TXEQ controls on CCI supported boards
 * Each array corresponds to one LWSwitch settings and each row of the array
 * corresponds to one OSFP 0 to 7
 *
 * Each OSFP setting has 12 bytes, first 4 bytes are precursor
 * bytes 4 to 7 are for postlwrsor
 * bytes 8 to 11 are for amplitude settings.
 *
 * each byte corresponds to 2 lanes, 4 bits for each lane, total 8 lanes.
 *
 * OSFP's supported by E4700 board: 6 (1-3, 5-7)
 * OSFP's supported for prospector: 8 (0-7)
 * OSFP's supported for wolf: 16 (0-15)
 *
 */

